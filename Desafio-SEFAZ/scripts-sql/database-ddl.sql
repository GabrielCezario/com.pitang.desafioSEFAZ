create table TELEFONES(

	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1),
	DDD INTEGER NOT NULL,
	NUMERO VARCHAR(250) NOT NULL,
	TIPO VARCHAR(250) NOT NULL
	ID_USUARIO INTEGER,
	
